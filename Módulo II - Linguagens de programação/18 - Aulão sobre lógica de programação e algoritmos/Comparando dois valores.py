# Comparando dois valores

numA = int(input("Digite o número A: "))
numB = int(input("Digite o número B: "))

if numA == numB :
    print("Os números são iguais.");

elif numA > numB : 
    print("O número " + numA + " é maior que " + numB)

else :
    print("O número " + numB + " é maior que " + numA)